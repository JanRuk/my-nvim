--[[
═══════════════════════════════════════════════════════════════════
  Treesitter Configuration
  
  Syntax highlighting, indentation, and text objects
═══════════════════════════════════════════════════════════════════
--]]

return {
  {
    "nvim-treesitter/nvim-treesitter",
    build = ":TSUpdate",
    event = { "BufReadPost", "BufNewFile" },
    dependencies = {
      "nvim-treesitter/nvim-treesitter-textobjects",
    },
    config = function()
      require("nvim-treesitter.configs").setup({
        -- Language parsers to install
        ensure_installed = {
          "lua",
          "vim",
          "vimdoc",
          "query",
          -- Java & JVM
          "java",
          "kotlin",
          "groovy",
          -- JavaScript/TypeScript
          "javascript",
          "typescript",
          "tsx",
          "jsdoc",
          -- Python
          "python",
          -- Web
          "html",
          "css",
          "scss",
          "json",
          "yaml",
          "toml",
          -- SQL & Docker
          "sql",
          "dockerfile",
          -- Markdown & docs
          "markdown",
          "markdown_inline",
          -- Config files
          "bash",
          "regex",
          "gitignore",
          "gitcommit",
          "diff",
        },
        
        -- Install parsers synchronously (only applied to `ensure_installed`)
        sync_install = false,
        
        -- Automatically install missing parsers when entering buffer
        auto_install = true,
        
        -- Ignore parsers for specific languages
        ignore_install = {},
        
        -- Modules
        highlight = {
          enable = true,
          disable = function(lang, buf)
            local max_filesize = 100 * 1024 -- 100 KB
            local ok, stats = pcall(vim.loop.fs_stat, vim.api.nvim_buf_get_name(buf))
            if ok and stats and stats.size > max_filesize then
              return true
            end
          end,
          additional_vim_regex_highlighting = false,
        },
        
        indent = {
          enable = true,
          disable = { "python", "yaml" }, -- Use LSP for these
        },
        
        incremental_selection = {
          enable = true,
          keymaps = {
            init_selection = "<C-space>",
            node_incremental = "<C-space>",
            scope_incremental = false,
            node_decremental = "<bs>",
          },
        },
        
        textobjects = {
          select = {
            enable = true,
            lookahead = true,
            keymaps = {
              ["af"] = "@function.outer",
              ["if"] = "@function.inner",
              ["ac"] = "@class.outer",
              ["ic"] = "@class.inner",
              ["aa"] = "@parameter.outer",
              ["ia"] = "@parameter.inner",
            },
          },
          move = {
            enable = true,
            set_jumps = true,
            goto_next_start = {
              ["]m"] = "@function.outer",
              ["]]"] = "@class.outer",
            },
            goto_next_end = {
              ["]M"] = "@function.outer",
              ["]["] = "@class.outer",
            },
            goto_previous_start = {
              ["[m"] = "@function.outer",
              ["[["] = "@class.outer",
            },
            goto_previous_end = {
              ["[M"] = "@function.outer",
              ["[]"] = "@class.outer",
            },
          },
        },
      })
    end,
  },
  
  -- ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  -- Context (show function/class context at top)
  -- ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  {
    "nvim-treesitter/nvim-treesitter-context",
    event = { "BufReadPost", "BufNewFile" },
    opts = {
      enable = true,
      max_lines = 3,
      min_window_height = 20,
      trim_scope = "outer",
      mode = "cursor",
    },
  },
}